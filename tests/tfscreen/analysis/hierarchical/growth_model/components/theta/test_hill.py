def test_run_model_calculation(mock_data):
    """
    Strict Hill equation check: K=0, n=1 → occupancy = sigmoid(log_conc).
    """
    T, G = mock_data.num_titrant_name, mock_data.num_genotype
    C = 3

    theta_param = ThetaParam(
        theta_low=jnp.zeros((T, G)),
        theta_high=jnp.ones((T, G)),
        log_hill_K=jnp.zeros((T, G)),
        hill_n=jnp.ones((T, G)),
        mu=jnp.zeros((T, C, 1)),
        sigma=jnp.ones((T, C, 1)),
    )

    data = mock_data._replace(log_titrant_conc=jnp.array([-10.0, 0.0, 10.0]),
                               scatter_theta=0)
    result = run_model(theta_param, data)

    assert jnp.allclose(result[:, 0, :], 0.0, atol=1e-3)
    assert jnp.allclose(result[:, 1, :], 0.5)
    assert jnp.allclose(result[:, 2, :], 1.0, atol=1e-3)


def test_run_model_scatter(theta_param_setup, mock_data):
    """scatter_theta=1 inserts four leading singleton dims."""
    result = run_model(theta_param_setup, mock_data._replace(scatter_theta=1))
    T, C, G = (mock_data.num_titrant_name,
               mock_data.num_titrant_conc,
               mock_data.num_genotype)
    assert result.shape == (1, 1, 1, 1, T, C, G)


def test_run_model_no_scatter(theta_param_setup, mock_data):
    """scatter_theta=0 returns (T, C, G_subset)."""
    result = run_model(theta_param_setup, mock_data._replace(scatter_theta=0))
    T, C, G = (mock_data.num_titrant_name,
               mock_data.num_titrant_conc,
               mock_data.num_genotype)
    assert result.shape == (T, C, G)


def test_run_model_batch_idx_translation(mock_data):
    """run_model uses data.batch_idx[geno_theta_idx], not geno_theta_idx directly."""
    T, G = mock_data.num_titrant_name, mock_data.num_genotype
    C = mock_data.num_titrant_conc

    # Distinct theta_low per genotype so we can tell them apart
    theta_low = jnp.arange(T * G, dtype=float).reshape(T, G)
    theta_param = ThetaParam(
        theta_low=theta_low,
        theta_high=jnp.ones((T, G)),
        log_hill_K=jnp.full((T, G), 1e6),    # very high K → occupancy ≈ 0 → theta ≈ theta_low
        hill_n=jnp.ones((T, G)),
        mu=jnp.zeros((T, C, 1)),
        sigma=jnp.ones((T, C, 1)),
    )

    # batch_idx = [2, 0, 1]: batch position 0 → genotype 2, etc.
    batch_idx     = jnp.array([2, 0, 1], dtype=jnp.int32)
    geno_theta_idx = jnp.array([0, 1],   dtype=jnp.int32)  # want batch positions 0, 1

    data = mock_data._replace(batch_idx=batch_idx,
                               geno_theta_idx=geno_theta_idx,
                               scatter_theta=0)
    result = run_model(theta_param, data)

    # batch positions 0 and 1 map to genotypes 2 and 0
    # At very high K, occupancy ≈ 0, so theta ≈ theta_low
    expected_theta_low = theta_low[:, jnp.array([2, 0])]
    assert jnp.allclose(result[:, 0, :], expected_theta_low, atol=1e-4)


